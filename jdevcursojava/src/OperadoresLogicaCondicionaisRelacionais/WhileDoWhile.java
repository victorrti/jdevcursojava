package OperadoresLogicaCondicionaisRelacionais;

public class WhileDoWhile {
	public static void main(String[] args) {
		int contador =0;
		while(contador < 20) {
			contador++;
			System.out.println(contador);
			
		}
		System.out.println(contador);
		int contador2 = 0;
		do {
			contador2++;
			System.out.println(contador2);
			
		}while(contador2 < 20);
		System.out.println(contador2);
	}
	
	
	

}
