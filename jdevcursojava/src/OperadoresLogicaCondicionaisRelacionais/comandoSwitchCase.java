package OperadoresLogicaCondicionaisRelacionais;

public class comandoSwitchCase {
	public static void main(String[] args) {
		int opcaoramal = 2;

		switch (opcaoramal) {
		case 1:
			System.out.println("setor tecnologia");
			break;
		case 2: 
			System.out.println("setor suporte");
			break;
		case 3: 
			System.out.println("setor financeiro");
			break;
		case 4: 
			System.out.println("setor vendas");
			break;
		default :
			System.out.println("opção invlida");
			break;
			
		}

	}
	
   
}
