package OperadoresLogicaCondicionaisRelacionais;

public class OperadoresRelacionais {
	public static void main(String[] args) {
		double nota1 = 70;
		double nota2 = 50;
		
		System.out.println(nota1==nota2);
		System.out.println(nota1>nota2);
		System.out.println(nota1>=nota2);
		System.out.println(nota1<nota2);
		System.out.println(nota1<=nota2);
		System.out.println(nota1!=nota2);
		
		if(nota1>nota2) {
			System.out.println("nota1 e  maior que nota 2");
		}

	}
	
	
		
		
		
		
	
}
